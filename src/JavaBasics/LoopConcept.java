package JavaBasics;

public class LoopConcept {

	public static void main(String[] args) {
	//1. While loop; disadvante:- will generate infinte loop if you dont give increment.
		int i=1; //initialization
		while(i<=10) {//conditional 
			System.out.println(i);
			i=i+1; //incrementatl // if this is remove it will generate infinite value
		}
		System.out.println("*******************");
	//2.For loop;
		for(int j=0;j<=10;j=j+2) {
			System.out.println(j);
		}
	//3. do while loop
		
	//4.

	}

}
