package SuperKeywordConcept;

public class A {
  public A() {
	  System.out.println("Parent class constr");
  }
  public A(int i) {
	  System.out.println("Parent class constr with value of i "+i);
  }
  public A(int i, int j) {
	  System.out.println("Parent class constr with value of i "+i);
	  System.out.println("Parent class constr with value of j "+j);
  }
}
